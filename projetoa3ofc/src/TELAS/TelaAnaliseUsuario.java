    private void updateData() {
        
        String nome = txt_nome.getText();
        String usuario = txt_usuario.getText();
        String senha = txt_senha.getText();
        String tel = txt_num.getText();
        String ender = txt_end.getText();
        String email = txt_email.getText();

        try (java.sql.Connection con = new ConnectionFactory().obtemConexao()) {
            String sql = "UPDATE usuariobda3 SET nome = ?, senha = ?, tel = ?, ender = ?, email = ? WHERE usuario = ?";
            java.sql.PreparedStatement stmt = con.prepareStatement(sql);
            stmt.setString(1, nome);
            stmt.setString(2, senha);
            stmt.setString(3, tel);
            stmt.setString(4, ender);
            stmt.setString(5, email);
            stmt.setString(6, usuario);
            int rowsAffected = stmt.executeUpdate();

            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Dados atualizados com sucesso!");
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao atualizar dados.");
            }
        } catch(Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Erro: " + ex.getMessage());
        }
    }
