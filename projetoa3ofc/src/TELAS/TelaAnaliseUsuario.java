    private void deleteUser() {
        String usuario = txt_usuario.getText();
        String senha = txt_senha.getText();

        if (usuario.isEmpty() || senha.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Todos os campos devem ser preenchidos.");
            return;
        }

        try (java.sql.Connection con = new ConnectionFactory().obtemConexao()) {
            String selectSql = "SELECT senha FROM usuariobda3 WHERE usuario = ?";
            java.sql.PreparedStatement selectStmt = con.prepareStatement(selectSql);
            selectStmt.setString(1, usuario);
            java.sql.ResultSet rs = selectStmt.executeQuery();

            if (rs.next()) {
                String senhaCorreta = rs.getString("senha");
                if (senha.equals(senhaCorreta)) {
                    String deleteSql = "DELETE FROM usuariobda3 WHERE usuario = ?";
                    java.sql.PreparedStatement deleteStmt = con.prepareStatement(deleteSql);
                    deleteStmt.setString(1, usuario);
                    int rowsAffected = deleteStmt.executeUpdate();

                    if (rowsAffected > 0) {
                        JOptionPane.showMessageDialog(this, "Usuário excluído com sucesso!");
                    } else {
                        JOptionPane.showMessageDialog(this, "Erro ao excluir usuário.");
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Senha incorreta.");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Usuário não encontrado.");
            }
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Erro ao excluir usuário: " + ex.getMessage());
        }
    }
    
